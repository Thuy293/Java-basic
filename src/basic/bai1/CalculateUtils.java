package basic.bai1;

public class CalculateUtils {
	public double x;
	public double y;

	public double tinhCong() {
		double a = x + y;
		return a;
	}
	public double tinhNhan() {
		double b = x*y;
		return b;
	}
	public double tinhTru() {
		double c = x - y;
		return c;
	}
	public double tinhChia() {
		double d = x/y;
		return d;
	}

}
